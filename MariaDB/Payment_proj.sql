create database payment_project;
use payment_project;


create table customer_info(
	cust_id Bigint primary key,
	name varchar(50),
	clear_balance int(10),
	overdraft enum('yes','no'),
	BIC varchar(11),
	foreign key(BIC) references Bank(BIC)
); 



create table Bank(
	BIC varchar(16) primary Key,
	bank_name varchar(100)
);


create table msg_info(
	message varchar(10) primary key,
	description varchar(500)
);



create table currency(
	cur varchar(3) primary key,
	value int
);


